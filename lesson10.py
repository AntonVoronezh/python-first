s = 'tuiyYotW79 kyvi'

print(len(s))

print(s.capitalize()) # Tuiyyotw79

print(s.center(20, '*')) # *****tuiyYotW79*****

print(s.count('t')) # 2

print(s.find('t')) # 0 - позиция первого вхождения, если нет то ошибка
print(s.replace('t', '*')) # *uiyYo*W79
print(s.split()) # ['tuiyYotW79', 'kyvi']
print(s.isdigit()) # False




